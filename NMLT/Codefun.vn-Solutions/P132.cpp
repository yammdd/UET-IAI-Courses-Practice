#include <iostream>
using namespace std;

bool isPrime(int n) {
    if (n <= 1) {
        return false;}
    for (int i = 2; i * i <= n; i++) {
        if (n % i == 0) {
            return false;
        }
    }
    return true;
}
int gcd(int a, int b) {
    if (b == 0) {
        return a;
    }
    return gcd(b, a % b);
}

int main() {
    int a, b;
    cin >> a;
    cin >> b;
    int gcd_ = gcd(a, b);
    if(isPrime(gcd_)) cout << "yes";
    else cout << "no";
    return 0;
}
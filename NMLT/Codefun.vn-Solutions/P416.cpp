#include <iostream>
#include <cmath>
using namespace std;
unsigned long long fermatNumber(int n) {
    return pow(2, pow(2, n)) + 1;
}
int main() {
    int n;
    cin >> n;
    unsigned long long result = fermatNumber(n);
    cout << result << endl;
    return 0;
}
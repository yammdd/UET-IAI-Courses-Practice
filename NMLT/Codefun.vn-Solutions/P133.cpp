#include <iostream>
using namespace std;

int main(){
    int n, count = 0;
    cin >> n;
    int x[n], y[n];
    for (int i = 0; i < n; i++) {
        cin >> x[i] >> y[i];
        if(y[i] - x[i] > 1) count++;
    }
    cout << count;
    return 0;
}
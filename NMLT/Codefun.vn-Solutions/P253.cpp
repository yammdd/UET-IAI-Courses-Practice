#include <iostream>
#include <string>
using namespace std;

int main() {
    string s;
    getline(cin, s);
    int count = 0;
    char c;
    cin >> c;
    for (int i = 0; i < s.length(); i++) {
        if (s[i] == c) count++;
    }
    cout << count << endl;
    return 0;
}
 
#include <iostream>
using namespace std;

int main() {
    int n, m, sum = 0;
    cin >> n >> m;
    int a[n][m];
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            cin >> a[i][j];
            if((i + j) % 2 == 0) sum += a[i][j];
        }
    }
    cout << sum;
    return 0;
}
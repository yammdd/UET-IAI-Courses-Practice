#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b;
    a = a % b;
    for (int i = 1; i < 70; ++i){
        a *= 10;
        a %= b;
    }
    cout << (a * 10) / b;
}

#include <bits/stdc++.h>
using namespace std;

int main() {
    int n, cnt;
    cin >> n;
    if (n < 3) {
        cnt = 0;
    }
    else {
        cnt = n - 2;
        for (int i = 1; i < n - 2; i = i + 2)
        {
            cnt += 2;
        }
    }
    cout << cnt;
    return 0;
}
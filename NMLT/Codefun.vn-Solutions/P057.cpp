#include <iostream>
using namespace std;

int main() {
    int n, count = 0;
    cin >> n;
    for (int i = 1; i <= n; i++) {
        if (n % i == 0) {
            count++;
        }
    }
    cout << (count % 2 == 0 ? count / 2 : (count - 1) / 2 + 1);
    return 0;
}

#include<iostream>
using namespace std;

int main() {
    int m, n;
    cin >> n >> m;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n-1; j++) {
            cout << m << " ";
        }
        cout << m << endl;
    }
    return 0;
}
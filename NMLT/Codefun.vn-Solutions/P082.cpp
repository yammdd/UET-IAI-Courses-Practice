#include <iostream>
using namespace std;

int main() {
    int a, b, c;
    cin >> a >> b >> c;
    long long product = 1;
    for (int i = a; i <= b; ++i) {
        product *= i;
        product %= c; 
    }
    cout << product;
    return 0;
}
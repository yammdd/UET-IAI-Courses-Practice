#include <iostream>
#include <vector>
using namespace std;

int main() {
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; ++i) {
        cin >> arr[i];
    }
    int m;
    cin >> m;
    int i = 0;
    while (m > 0) {
        if (arr[i] % 2 == 0) {
            m -= 1;
        }
        if (m != 0) {
            cout << arr[i] << " ";
        }
        i += 1;
        if (i == arr.size()) {
            break;
        }
    }
    return 0;
}
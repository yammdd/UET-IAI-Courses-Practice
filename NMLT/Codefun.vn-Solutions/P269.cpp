#include <iostream>
using namespace std;

int main() {
    long long n, x = 0;
    cin >> n;
    if (n > 100 || n <= 0) return 0;
    int a[n][n];
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> a[i][j];
        }
    }
    for (int i = 0; i < n; i++) {
        x += a[i][i];
        x += a[i][n-1-i];
    }
    if (n % 2 == 1) x -= a[(n-1)/2][(n-1)/2];
    cout << x;
    return 0;
}
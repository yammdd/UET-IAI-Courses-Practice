#include<bits/stdc++.h>
using namespace std;

int main() {
    int n, sum = 2;
    cin >> n;
    if (n == 1) cout << 1;
    if (n == 2) cout << 2;
    if (n >= 3) {
        int fn, f1 = 1, f2 = 1;
        for (int i = 3; i <= n; ++i) {
            fn = f1 + f2;
            sum += fn;
            f1 = f2;
            f2 = fn;
        }
        cout << sum;
    }
    return 0;
}
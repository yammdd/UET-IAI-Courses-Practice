#include <bits/stdc++.h>
using namespace std;

int main() {
    int n, m, p = 1;
    cin >> n >> m;
    for (int i = n; i <= m; i++) {
        if (i % 2 == 1) p *= i;
    }
    cout << p;
    return 0;
}
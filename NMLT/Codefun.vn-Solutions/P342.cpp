#include <iostream>
using namespace std;

int main() {
    int n, count = 1;
    cin >> n;
    if (n <= 0 || n > 20) return 0;
    for (int i = 1; i <= n; i += 2) {
        count *= i;
    }
    cout << count;
    return 0;
}
#include <iostream>
using namespace std;

int main() {
    string s;
    int n, i, j;
    cin >> s >> n;
    for (int x = 0; x < n; x++) {
        cin >> i >> j;
        swap(s[i-1], s[j-1]);
    }
    cout << s;
    return 0;
}
#include <iostream>
using namespace std;

int main() {
    int n, a, s = 1;
    cin >> n;

    for (int i = 1; i <= n; ++i) {
        cin >> a;
        s *= a;
        s %= 10;
    }
    cout << abs(s) << endl;
    return 0;
}
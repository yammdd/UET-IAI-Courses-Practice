#include <iostream>
using namespace std;

int main() {
    int x1, y1, x2, y2, x3, y3;
    cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3;

    if ((double)(x1 - x2) / (x3 - x2) == (double)(y1 - y2) / (y3 - y2))
        cout << "1";
    else
        cout << "0";
    return 0;
}
#include <bits/stdc++.h>
using namespace std;

int main(){
    long long a, b, c, temp = 1;
    cin >> a >> b >> c;
    for (long long i = 1; i <= b; i++) {
        temp *= a;
        temp %= c;
    }
    cout << temp;
    return 0;
}
#include <bits/stdc++.h>
using namespace std;

int main () {
    int n, x, y;
    cin >> n >> x >> y;
    if (n % 2 == 1) {
        if ((x + y) % 2 == 1) {
            cout << n * n / 2;
        } else {
            cout << n * n / 2 + 1;
        }
    } else {
        cout << n * n/2;
    }
    return 0;
}
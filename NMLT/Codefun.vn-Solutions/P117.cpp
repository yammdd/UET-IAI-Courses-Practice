#include <iostream>
using namespace std;

int main() {
    int h, m, count = 0;
    for (int i = 1; i < 7; ++i){
        cin >> h >> m;
        if (h < 1 || h > 24 || m < 0 || m > 60) return 0;
        if (h == 7 && m > 5 || h > 7) count++;
    }
    if (count > 2) cout << ":'(";
    else cout << ":)";
}
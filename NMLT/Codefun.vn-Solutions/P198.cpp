#include <iostream>
using namespace std;

int main() {
    int n, m;
    cin >> n >> m;
    int a[n];
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int j = 0; j < n; j++) {
        cout << (m / a[j]) << endl;
        m = m - (m / a[j]) * a[j];
    }
    return 0;
}
#include <iostream>
#include <string>
using namespace std;

int main() {
    string s;
    int n, a;
    char c;
    cin >> s >> n;
    for (int i = 0; i < n; i++) {
        cin >> a >> c;
        if (s[a-1] == c) cout << 1;
        else cout << 0;
    }
    return 0;
}
#include <iostream>
using namespace std;

int main() {
    int n, p = 1;
    cin >> n;
    int a[n][n];
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> a[i][j];
            if ((i == j) || ((i + j) == n - 1)) p *= a[i][j];
            if (a[i][j] > 5) return 0;
        }
    }
    if (n % 2 == 1 && a[(n - 1) / 2][(n - 1) / 2] != 0) p /= a[(n - 1) / 2][(n - 1) / 2];
    cout << p;
    return 0;
}
#include <bits/stdc++.h>
using namespace std;

int main() {
    int n, m, fac[100005], ans = 0;
    cin >> n >> m;
    fac[0] = 1;
    for (int i = 1; i <= n; i++) {
        fac[i] = (fac[i-1] * i) % m;
        ans += fac[i] % m;
        ans %= m;
    }
    cout << ans;
    return 0;
}
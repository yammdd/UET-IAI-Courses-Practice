#include <bits/stdc++.h>
using namespace std;

int main() {
    long long n, count, i;
    cin >> n;
    count = 0;
    i = 1;
    while (n >= i) {
        count += n - i + 1;
        i *= 10;
    }
    cout << count;
    return 0;
}
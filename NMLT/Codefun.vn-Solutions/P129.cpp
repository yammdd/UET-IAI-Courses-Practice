#include <iostream>
using namespace std;

bool isPrime(int n) {
    if (n <= 1) {
        return false;
    }
    for (int i = 2; i * i <= n; i++) {
        if (n % i == 0) {
            return false;
        }
    }
    return true;
}
int main() {
    int n, count= 0;
    cin >> n;
    if (isPrime(n)) {
        while (n > 0) {
            n /= 10;
            count++;
        }
        cout << "Good" << endl;
        cout << count;
    }
    else cout << "Bad";
    return 0;
}